import 'package:test/test.dart';
import '../bin/main.dart';

void main() {
  test('Towers of Hanoi', () {
    expect(possibleMoves([[3, 4], [1, 2], []]),
        [[[4], [1, 2], [3]], [[1, 3, 4], [2], []], [[3, 4], [2], [1]]]);
  });
  test('Towers of Hanoi', () {
    expect(possibleMoves([[1,2,3, 4], [], []]),
        [
          [[4], [1, 2], [3]],
          [[1, 3, 4], [2], []],
          [[3, 4], [2], [1]],
          [[2, 3, 4], [1], []],
          [[2, 3, 4], [], [1]]
        ]);
  });
  test('Towers of Hanoi', () {
    expect(possibleMoves([[1,4], [2], [3]]),
        [
          [[4], [1, 2], [3]],
          [[1, 3, 4], [2], []],
          [[3, 4], [2], [1]],
          [[2, 3, 4], [1], []],
          [[2, 3, 4], [], [1]],
          [[4], [1, 2], [3]],
          [[4], [2], [1, 3]],
          [[1, 4], [], [2, 3]]
        ]);
  });
}